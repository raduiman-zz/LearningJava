class WhileExample {
    public  static void main(String [] args) {
        int i = 1;
        while(i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}

class InfWhile {
    public static void main(String [] args) {
        while(true) {
            System.out.println("infinitive while loop");
        }
    }
}

